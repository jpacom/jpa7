                    <p class="title_h2">انیمیشن بدون دوربین (Animation Without Camera):</p>
                    <p style="margin: 0px;">یکی از ساده ترین و ارزان ترین راههای ساخت فیلم انیمیشن ، شاید همین روش باشد. زیرا می توان با استفاده از نوار فیلم خام یا نور خورده، بدون استفاده از دوربین و مراحل لابراتوری، فیلم ساخت.</br>
                        اگر فیلم خام را ظاهر کنیم و یا فیلم های قدیمی و مستعمل را با مقداری آب ژاول شستشو دهیم ، نوار شفافی به دست می آید که می توانیم با ابزار مختلفی مثل مداد چرب، مرکب، رنگ روغن و یا وسایل دیگری ، روی هرفریم آن پی درپی طراحی کنیم که حاصل آن ، فیلم انیمیشن است، همچنین با ایجاد خراش روی فیلم می توان انیمیشن های خالص، زیبا و موثری را خلق کرد. برای اجرای این روش از فیلم 16 میلیمتری می توان استفاده کرد ، ولی بهتراست برای تسهیل کار و گرفتن نتیجه بهتر از کادرهای بزرگتری مثل 35 میلیمتری و یا حتی 70 میلیمتری استفاده شود.</br>
